package com.differntmethods;
// different methods with differnt return type.
public class DiiferentMethodsDemo {
	// int is  return type  of additionOfNumber.
	public  static  int additionOfNumber(int a,int b) {
		int c = a+b;
		return c;
	}
	public static void main(String[] args) {
		
	int addition = additionOfNumber(5, 4);
	System.out.println("Addition of two number is:"+addition);
	}

}
